/**
 * WHISPER_HALLUCINATIONS — паттерны галлюцинаций Whisper для удаления из транскриптов.
 *
 * Whisper иногда генерирует повторяющиеся фразы, артефакты субтитров и
 * прочий шум, не связанный с реальным аудио.
 */
export const WHISPER_HALLUCINATIONS: RegExp[] = [
  // Артефакт субтитров — частая галлюцинация Whisper на русском аудио
  /субтитры\s+сделал\s+DimaTorzok/gi,
  /субтитры\s+сделаны\s+DimaTorzok/gi,
  /субтитры\s+(?:от|by)\s+DimaTorzok/gi,

  // Вариации с разным регистром / латиницей
  /DimaTorzok/gi,

  // Повторяющийся текст — признак галлюцинации (3+ повтора одного слова подряд)
  /(\b\w{3,}\b)(?:\s+\1){2,}/gi,

  // Типичные Whisper-заглушки на тишину
  /\[?\bмузыка\b\]?/gi,
  /\[?\bтишина\b\]?/gi,
  /\[?\bаплодисменты\b\]?/gi,
  /\[?\bsmooth music\b\]?/gi,
  /\[?\bmusic\b\]?/gi,
  /\[?\bapplause\b\]?/gi,
  /\[?\bsilence\b\]?/gi,
];
