/**
 * Единый источник промпта для извлечения insight-карточек из транскрипта.
 * Используется и автоматическим анализом (openrouter.ts), и ручной кнопкой
 * PasteInsightsButton (тренер копирует промпт в Claude вручную).
 *
 * Формат вывода жёстко завязан на parser parseInsightsMarkdown — менять
 * правила формата нужно синхронно с ним.
 */

export type ChatMessage = { role: "system" | "user"; content: string };

export const INSIGHTS_PROMPT = `Ты помогаешь тренеру по паделю. Прочитай транскрипт тренировки, который приведён в следующем сообщении, и выдели практические инсайты для ученика.

ВЫВЕДИ РЕЗУЛЬТАТ СТРОГО В ЭТОМ ФОРМАТЕ (никаких таблиц, нумерации, bold-выделений):

## Карточка 1
- Тема: техника
- Заголовок: <короткий совет, до 80 знаков>
- Описание: <конкретное действие, до 400 знаков>
- Цитата: "<дословная фраза из транскрипта>"

## Карточка 2
- Тема: тактика
- Заголовок: ...
- Описание: ...
- Цитата: "..."

ПРАВИЛА:
- Тема обязательно одна из: техника, тактика, физика, менталка (строчные буквы)
- Минимум 5, максимум 15 карточек
- В каждой карточке цитата — дословная фраза из транскрипта (без неё инсайт невалиден)
- Только actionable советы, не пересказ
- Никакого markdown-форматирования внутри полей (**bold**, *italic* и т.п.)
- Никаких таблиц, нумерованных списков, заголовков # или ###`;

/** Пример формата для UI-подсказки в PasteInsightsButton. */
export const INSIGHTS_FORMAT_EXAMPLE = `## Карточка 1
- Тема: техника
- Заголовок: Поздний замах при бандеха
- Описание: Начинай замах когда мяч ещё летит — на 0.5 сек раньше.
- Цитата: "ты опять поздно взял"

## Карточка 2
- Тема: тактика
- Заголовок: Держи позицию у сетки
- Описание: После удара смещайся к сетке, не оставайся у задней линии.
- Цитата: «не стой сзади, иди вперёд»`;

/** Собирает сообщения для chat-completions API: системный промпт + текст транскрипта. */
export function buildInsightsMessages(transcript: string): ChatMessage[] {
  return [
    { role: "system", content: INSIGHTS_PROMPT },
    { role: "user", content: transcript },
  ];
}
